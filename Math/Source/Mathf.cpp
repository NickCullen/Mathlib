#include "MathLib.h"
#include <math.h>

float Mathf::Sqrt(float val)
{
	return sqrtf(val);
}

float Mathf::Sin(float val)
{
	return sinf(val);
}
float Mathf::Cos(float val)
{
	return cosf(val);
}
float Mathf::Tan(float val)
{
	return tanf(val);
}
float Mathf::Atan2(float y, float x)
{
	return atan2f(y, x);
}